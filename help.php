<h2>Как зарегистрироваться в MasterCard?</h2>
<p>
<img width="150px" height="100px" src="http://xn--80aamczj4acek.ml/help1.PNG" style="display:inline;" class="righthelp">
Регистрация в MasterCard чрезвычайно проста: достаточно ввести своё ФИО, адрес электронной почты(на него будут поступать чеки) и CVC2- пароль из 3 цифр.</br>
<b>Важно!</b> Зарегистрировать второй счёт штатными средствами без сервисного центра нельзя! Для таких целей мы рекомендуем использовать услуги.
</p>
</div>
<div align="left">
<h2>Что даёт регистрация?</h2>
<p>
<img width="150px" height="100px" src="http://xn--80aamczj4acek.ml/help2.PNG" style="display:inline;" class="lefthelp">
После регистрации в личном кабинете у вас появляется много возможностей. Среди них: C2C(переводы с карты на карту), просмотр истории, дополнительный FAQ, возможность пополнения счёта и создание услуг.
</p>
</div>
<div align="right">
<h2>С чего начать?</h2>
<p>
<img width="150px" height="100px" src="http://xn--80aamczj4acek.ml/help3.PNG" style="display:inline;" class="righthelp">
Разумеется, стоить начать с пополнения счёта. Способ прост до невозможности: делаете заказ на любую сумму, потом в личном кабинете нажимаете кнопку "Пополнение" и вводите номер заказа, который был написан на чеке, а потом нажмите на кнопку "Пополнить счёт". Учтите, что использовать 1 код можно только 1 раз.
</p>
